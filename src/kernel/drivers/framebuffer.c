{
    uint32_t glyph_index;
    uint32_t px, py;
    const uint8_t *glyph;

    if (c < FONT_FIRST || c > FONT_LAST)
        glyph_index = 0; /* space for unprintable */
    else
        glyph_index = (uint32_t)(c - FONT_FIRST);

    glyph = font_data[glyph_index];

    for (py = 0; py < FONT_HEIGHT; py++) {
        uint8_t row = glyph[py];
        for (px = 0; px < FONT_WIDTH; px++) {
            uint32_t color = (row & (0x80 >> px)) ? fg_color : bg_color;
            fb_put_pixel(cx * FONT_WIDTH + px, cy * FONT_HEIGHT + py, color);
        }
    }
}

void fb_scroll(void)
{
    uint32_t y;
    uint8_t *dst;
    uint8_t *src;
    uint32_t row_bytes = fb_pitch * FONT_HEIGHT;

    if (!fb_ready)
        return;

    /* Copy each row up by one text line */
    for (y = 0; y < (max_rows - 1); y++) {
        dst = (uint8_t *)fb_addr + y * row_bytes;
        src = (uint8_t *)fb_addr + (y + 1) * row_bytes;

        /* Copy row_bytes worth of pixel data */
        uint32_t i;
        for (i = 0; i < row_bytes; i++) {
            dst[i] = src[i];
        }
    }

    /* Clear the last row */
    dst = (uint8_t *)fb_addr + (max_rows - 1) * row_bytes;
    uint32_t *pixel = (uint32_t *)dst;
    uint32_t pixels_in_row = (fb_pitch / (fb_bpp / 8)) * FONT_HEIGHT;
    uint32_t i;
    for (i = 0; i < pixels_in_row; i++) {
        pixel[i] = bg_color;
    }
}

void fb_putchar(char c)
{
    if (!fb_ready)
        return;

    switch (c) {
    case '\n':
        cursor_x = 0;
        cursor_y++;
        break;
    case '\r':
        cursor_x = 0;
        break;
    case '\t':
        cursor_x = (cursor_x + 4) & ~3u;
        break;
    case '\b':
        if (cursor_x > 0) {
            cursor_x--;
            fb_draw_char(cursor_x, cursor_y, ' ');
        }
        break;
    default:
        fb_draw_char(cursor_x, cursor_y, c);
        cursor_x++;
        break;
    }

    /* Line wrap */
    if (cursor_x >= max_cols) {
        cursor_x = 0;
        cursor_y++;
    }

    /* Scroll if needed */
    if (cursor_y >= max_rows) {
        fb_scroll();
        cursor_y = max_rows - 1;
    }
}

void fb_write(const char *str)
{
    if (!fb_ready)
        return;

    while (*str) {
        fb_putchar(*str++);
    }
}

void fb_set_color(uint32_t fg, uint32_t bg)
{
    fg_color = fg;
    bg_color = bg;
}
